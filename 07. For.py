x = range(2, 10, 2)
for n in x:
    print(f"número {n}")
    
# Esto significa que va a recorrer n hasta que la condición se cumpla, es decir hasta que n tome el valor de 10
# En este caso el primer número determina donde empieza la secuencia (Start)
# El segundo determina donde se detiene la secuencia (Stop)
# El tercero determina el paso (Step)
# Si se pone solo un número este cumplirá la función de Stop, el start será a partir de 0, y el step será de 1 de forma predeterminada. 
# n puede ser reemplazado por otra letra si quisieramnos ocupar otra