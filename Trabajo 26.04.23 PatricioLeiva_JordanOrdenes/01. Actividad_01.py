for i in range(5):
    print(i)
    
# La función range detemina que se hará un recorrido desde un número determinado
# en este caso 0, ya que es el número de partida por defecto, hasta el número que
# está entre paréntesis (5), el salto de números es 1 ya que este es el valor por defecto.
# "i" representa la variable que va a ir tomando estos valores
# por cada iteración. Por último el print nos mostrará el valor de "i" en cada iteración.